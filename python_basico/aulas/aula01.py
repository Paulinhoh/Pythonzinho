"""
O que é linguagem de programação?

"A linguagem de programação é utilizada para escrever algoritmos (sequências de passos para
resolver um problema ou realizar uma tarefa) compreensiveis ao computador. Esses algoritmos 
descrevem ao computador o que ele tem que fazer e como fazer.

Atualmente, existem muitas linguagens que são escritas pelos programadores. Essas linguagens
são classificadas em Linguagens de Baixo Nível e Linguagens de Alto Nível e são utilizadas
umas para aplicações gerais, que podem ser utilizadas para vários fins e outras mais 
especificas, que foram criadas de acordo com algum projeto exclusivo. Existem também as lingua-
gens de Nível Intermediário que contém caracteristicas tantro das linguagens de baixo nível,
quanto alto nível."
"""
