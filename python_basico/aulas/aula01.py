"""
O que é um algoritmo?

O conceito de algoritmo foi formalizado em 1936 por Alan Turing e Alonzo Church, que 
formaram as primeiras fundações da ciência da computação. Sendo esta formalização 
descrita a seguir:

"Um algoritmo é um conjunto não ambíguo e ordenado de passos executáveis que definem 
um processo finito".

Ou seja... uma receita de bolo pode ser um algoritmo.

O computador só faz o que nós mandamos ele fazer, então se ele está fazendo algo incrivel,
foi porque alguém programou. Do mesmo modo, se ele estiver falhando em alguma coisa, foi 
porque alguém programou com falha.
"""
