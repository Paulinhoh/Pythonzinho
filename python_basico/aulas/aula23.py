# Funções, argumentos e parâmetros

def soma(num1, num2):
    return num1 + num2

def subtracao(num1, num2):
    return num1 - num2

def multiplicacao(num1, num2):
    return num1 * num2

def divisao(num1, num2):
    return num1 / num2

print(f'soma -> {soma(10, 20)}')
print(f'soma -> {soma(6.5, 7)}')
print(f'subtração -> {subtracao(7, 3)}')
print(f'multiplicação -> {multiplicacao(8, 5)}')
print(f'divisão -> {divisao(10, 2)}')

